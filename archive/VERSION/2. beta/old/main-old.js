function updateWatchTime() {
    var secondsWatched = Math.floor(document.getElementById('video').currentTime);
    var minutesWatched = Math.floor(secondsWatched / 60)
    var secondsWithinMinute = secondsWatched - (minutesWatched * 60);
    if (secondsWithinMinute.toString().length == 1) {
        secondsWithinMinute = '0' + secondsWithinMinute.toString();
    }
    var minutesWithinHour = minutesWatched - (60 * Math.floor(minutesWatched / 60));
    if (minutesWithinHour.toString().length == 1) {
        minutesWithinHour = '0' + minutesWithinHour.toString();
    }
    if (minutesWatched >= 60) {
        minutesWatched = (Math.floor(minutesWatched / 60)) + ':' + minutesWithinHour;
    }
    document.getElementById('video-time-watched').innerHTML = minutesWatched + ':' + secondsWithinMinute;
    if ((localStorage['mediaType'] == 'tv') && localStorage['hasModifiedProgress'] == 'false') {
        localStorage[localStorage['tvShowTitleSpaced'] + ' progressTime'] = document.getElementById('video').currentTime;
    }
    if ((localStorage['mediaType'] == 'movie') && localStorage['hasModifiedProgress'] == 'false') {
        localStorage[localStorage['movieTitle'] + ' progressTime'] = document.getElementById('video').currentTime;
    }
    if (localStorage['hasModifiedProgress'] == 'false') {
        if (document.getElementById('video').currentTime > (document.getElementById('video').duration - 40)) {
            console.log('complete');
            if (localStorage['mediaType'] == 'tv') {
                localStorage[localStorage['tvShowTitleSpaced'] + ' progressTime'] = document.getElementById('video').currentTime;
                // localStorage[localStorage['tvShowTitleSpaced'] + ' progressEpisode'] = ;
                if (localStorage['tvEpisodeNum'] == localStorage['tvSeasonCapacity']) {
                    if (localStorage['tvSeasonNum'] == localStorage['tvSeasons']) {
                        linkAction('index');
                        localStorage[localStorage['tvShowTitleSpaced'] + ' watched'] = 'false';
                    } else {
                        localStorage[localStorage['tvShowTitleSpaced'] + ' progressEpisode'] = 1;
                        localStorage[localStorage['tvShowTitleSpaced'] + ' progressSeason'] = eval(localStorage['tvShowTitleUnspaced'] + 'S' + localStorage['tvSeasonNum'])[1];
                    }
                } else {
                    localStorage[localStorage['tvShowTitleSpaced'] + ' progressEpisode']++;
                }
            }
            if (localStorage['mediaType'] == 'movie') {
                localStorage[localStorage['movieTitle'] + ' progressTime'] = 0;
                localStorage[localStorage['movieTitle'] + ' watched'] = 'false';
            }
            localStorage['hasModifiedProgress'] = 'true';
            if (localStorage['mediaType'] == 'tv') {
                localStorage[localStorage['tvShowTitleSpaced'] + ' progressTime'] = 0;
            }
            if (localStorage['mediaType'] == 'media') {
                localStorage[localStorage['movieTitleSpaced'] + ' progressTime'] = 0;
            }
        }
    }
}

